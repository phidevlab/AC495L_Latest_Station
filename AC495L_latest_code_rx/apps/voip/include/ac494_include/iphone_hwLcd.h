#ifndef	__IPHONE_HW_LCD_h
#define	__IPHONE_HW_LCD_h






/****************************************************************************
*
*	Name:		lcdString
*----------------------------------------------------------------------------
*	Abstract:	send a string to the lcd
*----------------------------------------------------------------------------
*	Input:      message - string to display
*				size	- string len
*----------------------------------------------------------------------------
*  	Output:		none
*----------------------------------------------------------------------------
*	Returns: 	-1 in case of error
******************************************************************************/
void lcdString(char *message, int size);


/****************************************************************************
*
*	Name:		lcdClear
*----------------------------------------------------------------------------
*	Abstract:	clear the LCD display
*----------------------------------------------------------------------------
*	Input:      none
*----------------------------------------------------------------------------
*  	Output:		none
*----------------------------------------------------------------------------
*	Returns: 	-1 in case of error
******************************************************************************/
void lcdClear(void);


/****************************************************************************
*
*	Name:		lcdInit
*----------------------------------------------------------------------------
*	Abstract:	init the lcd
*----------------------------------------------------------------------------
*	Input:      none
*----------------------------------------------------------------------------
*  	Output:		none
*----------------------------------------------------------------------------
*	Returns: 	-1 in case of error
******************************************************************************/
void lcdInit(void);


/****************************************************************************
*
*	Name:		lcdMoveCursor
*----------------------------------------------------------------------------
*	Abstract:	Change the cursor position.
*----------------------------------------------------------------------------
*	Input:      none
*----------------------------------------------------------------------------
*  	Output:		none
*----------------------------------------------------------------------------
*	Returns: 	-1 in case of error
******************************************************************************/
void lcdMoveCursor(int row , int column);



/****************************************************************************
*
*	Name:		lcdClose
*----------------------------------------------------------------------------
*	Abstract:	close the lcd file descriptor
*----------------------------------------------------------------------------
*	Input:      none
*----------------------------------------------------------------------------
*  	Output:		none
*----------------------------------------------------------------------------
*	Returns: 	-1 in case of error
******************************************************************************/
void lcdClose(void);



#endif /* __IPHONE_HW_LCD_h*/


